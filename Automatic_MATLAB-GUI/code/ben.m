% --- Executes just before ben is made visible.
function ben_OpeningFcn(hObject, eventdata, handles, varargin)
% This function has no output args, see OutputFcn.
% hObject    handle to figure
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% varargin   command line arguments to ben (see VARARGIN)

% Choose default command line output for ben
handles.output = hObject;

% Update handles structure
guidata(hObject, handles);
axes(handles.axes1);
imshow('ben.png');
axes(handles.axes2);
imshow('benmo.png');
a=str2double(get(handles.edit5,'String'));%Wn2
c=str2double(get(handles.edit7,'String'));
num=[a];
den=[1,c,a];
t=[0:0.001:3];
axes(handles.axes3);
x=step(num,den,t);
hold off;
plot(t,x,'k');
grid on;
hold on;
d=sqrt(a);%wn
kx=c/(2*d);%kexi
set(handles.edit12,'String',kx);
mp=exp((-3.14*kx)/sqrt(1-kx^2));%Mp
set(handles.edit9,'String',mp);
tp=3.14/(d*sqrt(1-kx^2));%Tp
set(handles.edit10,'String',tp);
tr=(3.14-acos(kx))/(d*sqrt(1-kx^2));%Tr
set(handles.edit13,'String',tr);
ts5=3/(d*kx);
set(handles.edit11,'String',ts5);
ts2=4/(d*kx);
set(handles.edit14,'String',ts2);

